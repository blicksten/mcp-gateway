// Phase 7 — tests for `mcp-ctl doctor` (B-16). Strategy mirrors
// install_claude_code_test.go: fakeRunner injects `claude` calls,
// httptest.Server fakes the gateway, and a temp dir backs the
// extensions root via $GATEWAY_VSCODE_EXTENSIONS_DIR.

// doctorTestEnv composes all the moving pieces a single doctor.run()
// invocation needs. Returned doctor is wired with the temp extensions
// dir (no patched index.js by default — tests that need the patched
// branch call writePatchedIndexJS).
type doctorTestEnv struct {
	t            *testing.T
	gateway      *httptest.Server
	tokenPath    string
	tokenContent string
	extDir       string
	runner       *fakeRunner
	cmd          *cobra.Command
	doctor       *doctor
}

// makeDoctorTestEnv builds the standard happy-path environment.
//   - gateway: GET /api/v1/health -> 200
//   - token file: 32 chars
//   - extDir: empty (no patch); call writePatchedIndexJS(env, "1.18.0")
//     to add the marker file in the same dir.
//   - runner: claude --version succeeds; plugin list returns mcp-gateway
func makeDoctorTestEnv(t *testing.T) *doctorTestEnv {
	t.Helper()

	srv := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		switch r.URL.Path {
		case "/api/v1/health":
			w.WriteHeader(http.StatusOK)
			fmt.Fprint(w, `{"status":"ok"}`)
		default:
			w.WriteHeader(http.StatusNotFound)
		}
	}))
	t.Cleanup(srv.Close)

	tmpDir := t.TempDir()
	tokenPath := filepath.Join(tmpDir, "auth.token")
	const tok = "doctor-test-token-12345678901234"
	require.NoError(t, os.WriteFile(tokenPath, []byte(tok+"\n"), 0o600))

	extDir := filepath.Join(tmpDir, "extensions")
	require.NoError(t, os.MkdirAll(extDir, 0o755))

	fr := newFakeRunner()
	fr.respond("claude --version", &fakeCommand{stdout: []byte("Claude Code 2.1.123\n")})
	fr.respond("claude plugin list --json", &fakeCommand{
		stdout: []byte(`{"plugins":[{"name":"mcp-gateway","user_config":{"auth_token":"x"}}]}`),
	})

	cmd := newDoctorCmd()
	cmd.Flags().String("api-url", srv.URL, "")
	cmd.Flags().String("auth-token-file", tokenPath, "")

	doc := &doctor{
		runner:        fr.run,
		httpClient:    &http.Client{Timeout: 1 * time.Second},
		extensionsDir: extDir,
	}

	return &doctorTestEnv{
		t:            t,
		gateway:      srv,
		tokenPath:    tokenPath,
		tokenContent: tok,
		extDir:       extDir,
		runner:       fr,
		cmd:          cmd,
		doctor:       doc,
	}
}

// writePatchedIndexJS plants a patched anthropic.claude-code-X.Y.Z
// extension under env.extDir so checkPatchApplied finds the marker.
func writePatchedIndexJS(t *testing.T, env *doctorTestEnv, version string) {
	t.Helper()
	dir := filepath.Join(env.extDir, "anthropic.claude-code-"+version, "webview")
	require.NoError(t, os.MkdirAll(dir, 0o755))
	body := "// MCP Gateway Patch v" + version + " — autogenerated by apply-mcp-gateway.sh\n" +
		"console.log('hello world');\n"
	require.NoError(t, os.WriteFile(filepath.Join(dir, "index.js"), []byte(body), 0o644))
}

// resultByName finds a check by its name; fails the test when absent.
func resultByName(t *testing.T, results []checkResult, name string) checkResult {
	t.Helper()
	for _, r := range results {
		if r.name == name {
			return r
		}
	}
	t.Fatalf("no check named %q in results", name)
	return checkResult{}
}

// --- happy path ---

func TestDoctor_AllChecksPassWhenEnvironmentIsHealthy(t *testing.T) {
	env := makeDoctorTestEnv(t)
	writePatchedIndexJS(t, env, "1.18.0")

	results := env.doctor.run(env.cmd)
	require.Len(t, results, 5)

	for _, r := range results {
		assert.Equal(t, checkPass, r.status, "check %q should pass: detail=%s hint=%s", r.name, r.detail, r.hint)
	}
	assert.False(t, anyFailed(results))
}

// --- gateway down ---

func TestDoctor_GatewayUnreachable(t *testing.T) {
	env := makeDoctorTestEnv(t)
	env.gateway.Close() // tear down so /health connection is refused

	results := env.doctor.run(env.cmd)
	r := resultByName(t, results, "gateway reachable")
	assert.Equal(t, checkFail, r.status)
	assert.Contains(t, r.hint, "mcp-ctl daemon start")
}

func TestDoctor_GatewayReturns401(t *testing.T) {
	env := makeDoctorTestEnv(t)
	env.gateway.Close()
	srv := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, _ *http.Request) {
		w.WriteHeader(http.StatusUnauthorized)
	}))
	t.Cleanup(srv.Close)
	require.NoError(t, env.cmd.Flags().Set("api-url", srv.URL))

	results := env.doctor.run(env.cmd)
	r := resultByName(t, results, "gateway reachable")
	assert.Equal(t, checkFail, r.status)
	assert.Contains(t, r.hint, "--refresh-token")
}

