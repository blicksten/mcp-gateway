func TestDoctor_PatchPresent_PicksLatestVersionWhenMultiple(t *testing.T) {
	env := makeDoctorTestEnv(t)
	// Two extension versions: only the older one carries the marker.
	// Reverse alphabetical scan still finds it (because we iterate all).
	older := filepath.Join(env.extDir, "anthropic.claude-code-1.17.0", "webview")
	newer := filepath.Join(env.extDir, "anthropic.claude-code-1.18.0", "webview")
	require.NoError(t, os.MkdirAll(older, 0o755))
	require.NoError(t, os.MkdirAll(newer, 0o755))
	require.NoError(t, os.WriteFile(filepath.Join(older, "index.js"),
		[]byte("// MCP Gateway Patch v1.17.0\n"), 0o644))
	require.NoError(t, os.WriteFile(filepath.Join(newer, "index.js"),
		[]byte("// stock\n"), 0o644))

	results := env.doctor.run(env.cmd)
	r := resultByName(t, results, "webview patch")
	assert.Equal(t, checkPass, r.status)
	assert.Contains(t, r.detail, "1.17.0")
}

// --- exit-code wiring + table render ---

func TestDoctor_ExecuteCommand_ExitsZeroWhenAllPass(t *testing.T) {
	env := makeDoctorTestEnv(t)
	writePatchedIndexJS(t, env, "1.18.0")

	t.Setenv("GATEWAY_VSCODE_EXTENSIONS_DIR", env.extDir)
	out, err := executeCommand(env.gateway, "--auth-token-file", env.tokenPath, "doctor", "--no-color")
	// We can't substitute the doctor's commandRunner via executeCommand (it
	// uses realCommand), so we expect either:
	//   - exit 0 if the dev box has claude CLI + the plugin + the patched ext, OR
	//   - exit 1 with hints listed.
	// What we DO assert is that the table renders all 5 check rows and
	// that no Go panic / cobra usage error leaks out.
	if err != nil {
		var iee *installExitError
		require.True(t, errors.As(err, &iee), "non-installExitError leaked: %v", err)
		assert.Equal(t, installExitUsage, iee.Code)
	}
	for _, want := range []string{"gateway reachable", "auth token", "claude CLI", "plugin installed", "webview patch"} {
		assert.Contains(t, out, want, "doctor table should list %q", want)
	}
}

// --- isSemverByte / readPatchVersion edge cases ---

func TestReadPatchVersion_ParsesMarkerInBundle(t *testing.T) {
	dir := t.TempDir()
	indexJS := filepath.Join(dir, "index.js")
	body := strings.Repeat("/*pad*/", 100) + "// MCP Gateway Patch v1.16.0\n" +
		strings.Repeat("zzz", 200)
	require.NoError(t, os.WriteFile(indexJS, []byte(body), 0o644))

	ver, ok := readPatchVersion(indexJS)
	assert.True(t, ok)
	assert.Equal(t, "v1.16.0", ver)
}

func TestReadPatchVersion_MissingFileReturnsFalse(t *testing.T) {
	_, ok := readPatchVersion(filepath.Join(t.TempDir(), "absent.js"))
	assert.False(t, ok)
}

func TestReadPatchVersion_MarkerBeyondHeadCutoff(t *testing.T) {
	dir := t.TempDir()
	indexJS := filepath.Join(dir, "index.js")
	// Push the marker past 4 KiB on purpose — readPatchVersion only
	// scans the head, so this must NOT match.
	body := strings.Repeat("a", 5000) + "// MCP Gateway Patch v9.9.9\n"
	require.NoError(t, os.WriteFile(indexJS, []byte(body), 0o644))

	_, ok := readPatchVersion(indexJS)
	assert.False(t, ok)
}

func TestIsSemverByte(t *testing.T) {
	for _, b := range []byte{'0', '5', '9', '.'} {
		assert.True(t, isSemverByte(b), "byte %q should be semver", b)
	}
	for _, b := range []byte{'a', 'Z', '-', ' ', '\n', 'v'} {
		assert.False(t, isSemverByte(b), "byte %q should not be semver", b)
	}
}

