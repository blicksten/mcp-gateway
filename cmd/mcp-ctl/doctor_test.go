func TestCompareSemver_OrdersNumericallyNotLexically(t *testing.T) {
	// The bug this guards: lexicographic compare puts "1.10.0" BEFORE
	// "1.9.0", which would make checkPatchApplied report the older
	// version when both extension dirs are patched.
	cases := []struct {
		a, b string
		want int
	}{
		{"v1.18.0", "v1.17.0", 1},
		{"v1.17.0", "v1.18.0", -1},
		{"v1.10.0", "v1.9.0", 1},   // numeric order beats lex
		{"v1.9.0", "v1.10.0", -1},  // ditto
		{"v1.18.0", "v1.18.0", 0},
		{"v1.18", "v1.18.0", 0},    // missing components default to 0
		{"1.18.0", "v1.18.0", 0},   // 'v' prefix tolerated
		{"v2.0.0", "v1.99.99", 1},  // major dominates
	}
	for _, tc := range cases {
		got := compareSemver(tc.a, tc.b)
		assert.Equal(t, tc.want, got, "compareSemver(%q, %q)", tc.a, tc.b)
	}
}

func TestCheckPatchApplied_PicksHighestSemverWhenMultiplePatched(t *testing.T) {
	env := makeDoctorTestEnv(t)
	// Both 1.9.0 (older) and 1.10.0 (newer) carry markers. Lex order
	// says 1.10.0 < 1.9.0 because '0' < '9'; numeric order says 1.10.0
	// > 1.9.0. The detail line must report 1.10.0.
	for _, ver := range []string{"1.9.0", "1.10.0"} {
		dir := filepath.Join(env.extDir, "anthropic.claude-code-"+ver, "webview")
		require.NoError(t, os.MkdirAll(dir, 0o755))
		body := "// MCP Gateway Patch v" + ver + " — autogenerated\n"
		require.NoError(t, os.WriteFile(filepath.Join(dir, "index.js"), []byte(body), 0o644))
	}

	results := env.doctor.run(env.cmd)
	r := resultByName(t, results, "webview patch")
	assert.Equal(t, checkPass, r.status)
	assert.Contains(t, r.detail, "v1.10.0", "must pick highest semver, not lex-first")
	assert.NotContains(t, r.detail, "v1.9.0)", "must NOT report the older version")
}

